package upc.edu.pe.javanet.solicitudService;

public class Consultor {
	private int id;
	private String nomConsultor ;
    private String apeConsultor;
	private String categoria;
	private String especialidad;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNomConsultor() {
		return nomConsultor;
	}
	public void setNomConsultor(String nomConsultor) {
		this.nomConsultor = nomConsultor;
	}
	public String getApeConsultor() {
		return apeConsultor;
	}
	public void setApeConsultor(String apeConsultor) {
		this.apeConsultor = apeConsultor;
	}
	public String getCategoria() {
		return categoria;
	}
	public void setCategoria(String categoria) {
		this.categoria = categoria;
	}
	public String getEspecialidad() {
		return especialidad;
	}
	public void setEspecialidad(String especialidad) {
		this.especialidad = especialidad;
	}
	
	
	

}
